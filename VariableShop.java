public class VariableShop
{
	public static void main(String[] args)
	{
		byte Byte = 127;
		short Short = 32767;
		int Int = 214748364;
		long Long = 214748364;
		float Float = 1.55555f;
		double Double = 1.555555555555555;
		boolean Boolean = true;
		char Char = 'A';
		
		Byte = 60;
		Short = 55;
		Int = 420;
		Long = 69;
		Float = 1.9f;
		Double = 1.888;
		Boolean = false;
		Char = 'X';
		
		System.out.println("a byte is: " + Byte);
		System.out.println("a short is: " + Short);
		System.out.println("a int is: " + Int);
		System.out.println("a long is: " + Long);
		System.out.println("a float is: " + Float);
		System.out.println("a double is: " + Double);
		System.out.println("a boolean is: " + Boolean);
		System.out.println("a char is: " + Char);
	}
}